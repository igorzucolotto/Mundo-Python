# Dobro, triplo e raiz quadrada
# Crie um algoritmo que leia um número e mostre o seu dobro, o seu triplo e sua raiz quadrada

num = int(input(print("Digite um número inteiro >> ")));

dobro = num * 2;
triplo = num * 3;
raiz = num ** 0.5;

print("O dobro do número é: %d\nO triplo é: %d\nSua raiz é: %d" % (dobro, triplo, raiz));